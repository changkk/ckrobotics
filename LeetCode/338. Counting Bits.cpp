/*
    0 1 2
    0   => 0
    01  -> 1
    10  -> 1
    11  -> 2
    
    0   -> 0  // 0
    1   -> 1  // 1
    10  -> 1  // 2
    11  -> 2 // 2
    100 -> 1 // 3
    101 -> 2 // 3
    110 -> 2 // 3
    111 -> 3 // 3
    1000 -> 1 // 4  // 111 & 1000 -> 0000 + 1
    1001 // 2 // 4 // 1000 & 1001 -> 1000 -> number of 1 + 1 = 2
    1010 // 2 // 4 // 1001 & 1010 -> 1000 
    1011 // 3 // 4 // 1010<<1 => 10100 1011
    1100 // 2 // 4
    1101 // 3 // 4
    1110 // 3 // 4
    1111 // 4 // 4
    






*/



class Solution {
public:
    vector<int> countBits(int num) {
        vector<int> res(num+1,0);
        for(int i = 1; i<=num; i++)
            res[i] = res[i&(i-1)] + 1;
        
        return res;
    }
};